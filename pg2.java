public class pg2 {
    public static void main(String[] args) {
        double d = 8.88;
        System.out.println(d);

        byte b = 10;
        System.out.println(b);
        int i = b;
        System.out.println(i);

        //强制类型转换
        int k = (int)d;
        System.out.println(k);
    }
}
